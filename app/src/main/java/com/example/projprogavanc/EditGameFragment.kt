     *
     *
     * This will always be called from the process's main thread.
     *
     * @param loader The Loader that has finished.
     * @param data The data generated by the Loader.
     */
    override fun onLoadFinished(loader: Loader<Cursor>, data: Cursor?) {
        val adapterGameType = SimpleCursorAdapter(
            requireContext(),
            android.R.layout.simple_list_item_1,
            data,
            arrayOf(TDBGameTypes.C_TYPE),
            intArrayOf(android.R.id.text1),
            0
        )

        binding.spinnerGameType.adapter = adapterGameType

        updateSelectedGameType()
    }

    private fun updateSelectedGameType() {
        if(game == null) return

        val gameTypeId = game!!.type.id

        val lastGameType = binding.spinnerGameType.count - 1

        for (i in 0..lastGameType){
            if(binding.spinnerGameType.getItemIdAtPosition(i) == gameTypeId) {
                binding.spinnerGameType.setSelection(i)
                return
            }
        }
    }

    /**
     * Called when a previously created loader is being reset, and thus
     * making its data unavailable.  The application should at this point
     * remove any references it has to the Loader's data.
     *
     *
     * This will always be called from the process's main thread.
     *
     * @param loader The Loader that is being reset.
     */
    override fun onLoaderReset(loader: Loader<Cursor>) {
        if(_binding == null) return
        binding.spinnerGameType.adapter = null
    }

    fun processOptionMenu(item: MenuItem): Boolean =
        when(item.itemId){
            R.id.action_save ->{
                save()
                true
            }
            R.id.action_cancel ->{
                backtoGameList()
                true
            }
            else -> false
        }

    private fun save() {

        val gameName = binding.editTextGameName.text.toString()
        if(gameName.isBlank()){
            binding.editTextGameName.error = getString(R.string.name_error)
            binding.editTextGameName.requestFocus()
            return

        }

        val gameTypeId = binding.spinnerGameType.selectedItemId
        if(gameTypeId == Spinner.INVALID_ROW_ID){
            binding.textViewGame.error = getString(R.string.Type_error)
            binding.spinnerGameType.requestFocus()
            return

        }


        val savedGame =
            if(game == null){
                insertGame(gameName,gameTypeId)

            }else{
                editGame(gameName,gameTypeId)
            }


        if(savedGame){

            Toast.makeText(requireContext(), getString(R.string.save_game_success), Toast.LENGTH_LONG).show()
            backtoGameList()

        }else{
            Toast.makeText(requireContext(), R.string.General_error, Toast.LENGTH_LONG).show()

        }
    }

    private fun editGame(gameName: String,gameTypeId: Long): Boolean {
        val gameType = Uri.withAppendedPath(ContentProviderGameStore.GAMETYPES_ADDRESS, gameTypeId.toString())

        val SelectedGameType = requireActivity().contentResolver.query(gameType,TDBGameTypes.ALL_COLUMNS,"${TDBGameTypes.C_ID} = ?",arrayOf(gameTypeId.toString()),null)

        if((SelectedGameType == null)) return false
        assert(SelectedGameType.moveToNext())

        val game = Game(gameName,GameType.fromCursor(SelectedGameType))
        val gameAddress = Uri.withAppendedPath(ContentProviderGameStore.GAMES_ADDRESS,"${this.game!!.id}")

        val UpdatedEntries = requireActivity().contentResolver.update(gameAddress, game.toContentValues(),null,null)

        return UpdatedEntries == 1

    }

    private fun insertGame(
        gameName: String,
        gameTypeId: Long
    ):Boolean {

        val gameType = Uri.withAppendedPath(ContentProviderGameStore.GAMETYPES_ADDRESS, gameTypeId.toString())

        val SelectedGameType = requireActivity().contentResolver.query(gameType,TDBGameTypes.ALL_COLUMNS,"${TDBGameTypes.C_ID} = ?",arrayOf(gameTypeId.toString()),null)

        if((SelectedGameType == null)) return false
        assert(SelectedGameType.moveToNext())

        //TODO("Verify if a Game Store with same data already exists in the table if so return false")

        val game = Game(gameName,GameType.fromCursor(SelectedGameType))
        val insertedGameAddress = requireActivity().contentResolver.insert(ContentProviderGameStore.GAMES_ADDRESS, game.toContentValues()) ?: return false

        game.id = ContentUris.parseId(insertedGameAddress)

        return true

    }

    private fun backtoGameList() {
        (requireActivity() as MainActivity).updateTitle(getString(R.string.fragment_game_list))
        findNavController().navigate(R.id.action_EditGameFragment_to_GameList)
    }

}